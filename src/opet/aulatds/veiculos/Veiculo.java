package opet.aulatds.veiculos;

public class Veiculo {
	private int ano = 0;
	private String cor = "";
	
	public Veiculo()
	{
		
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}

}
